int main(void)
{
	char c;

	char d;

	c = 'a';
	d = 'A';
	while
		(c <= 'a') {
			putchar(c);
			c++;
		}
	while
		(c <= 'A') {
			putchar(d);
			d++;
		}
	putchar('\n');
	return (0);
}
